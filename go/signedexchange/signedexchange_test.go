type zeroReader struct{}

func (zeroReader) Read(b []byte) (int, error) {
	for i := range b {
		b[i] = 0
	}
	return len(b), nil
}

func mustReadFile(path string) []byte {
	b, err := ioutil.ReadFile(path)
	if err != nil {
		panic(err)
	}
	return b
}

// readableString converts an arbitrary value v to a string.
//
// readableString does a basically same thing as fmt.Sprintf("%q", v), but
// the difference is that map keys are ordered in alphabetical order so that
// the results are deterministic.
func readableString(v interface{}) string {
	switch v := v.(type) {
	case []interface{}:
		vals := []string{}
		for _, val := range v {
			vals = append(vals, readableString(val))
		}
		return "[" + strings.Join(vals, " ") + "]"
	case map[interface{}]interface{}:
		keys := []string{}
		// Assume that keys are strings.
		for k := range v {
			keys = append(keys, k.(string))
		}
		sort.Strings(keys)
		vals := []string{}
		for _, k := range keys {
			val := v[k]
			vals = append(vals, fmt.Sprintf("%q:", k)+readableString(val))
		}
		return "map[" + strings.Join(vals, " ") + "]"
	case string, []byte:
		return fmt.Sprintf("%q", v)
	default:
		panic(fmt.Sprintf("not supported type: %T", v))
	}
}

func TestSignedExchange(t *testing.T) {
	u, _ := url.Parse("https://example.com/")
	header := http.Header{}
	header.Add("Content-Type", "text/html; charset=utf-8")
	e, err := NewExchange(u, nil, 200, header, []byte(payload), 16)
	if err != nil {
		t.Fatal(err)
	}
	e.AddSignedHeadersHeader()

	now := time.Date(2018, 1, 31, 17, 13, 20, 0, time.UTC)
	certs, err := ParseCertificates([]byte(pemCerts))
	if err != nil {
		t.Fatal(err)
	}

	derPrivateKey, _ := pem.Decode([]byte(pemPrivateKey))
	privKey, err := ParsePrivateKey(derPrivateKey.Bytes)
	if err != nil {
		t.Fatal(err)
	}
	certUrl, _ := url.Parse("https://example.com/cert.msg")
	validityUrl, _ := url.Parse("https://example.com/resource.validity")
	s := &Signer{
		Date:        now,
		Expires:     now.Add(1 * time.Hour),
		Certs:       certs,
		CertUrl:     certUrl,
		ValidityUrl: validityUrl,
		PrivKey:     privKey,
		Rand:        zeroReader{},
	}
	if err := e.AddSignatureHeader(s); err != nil {
		t.Fatal(err)
	}

	var buf bytes.Buffer
	if err := WriteExchangeFile(&buf, e); err != nil {
		t.Fatal(err)
	}
	binExchange := buf.Bytes()

	// Decode length part
	cborLength := (int(binExchange[0]) << 16) | (int(binExchange[1]) << 8) | int(binExchange[2])

	// Deocde request/response header part
	var decoded interface{}
	handle := &codec.CborHandle{}
	if err := codec.NewDecoder(bytes.NewReader(binExchange[3:3+cborLength]), handle).Decode(&decoded); err != nil {
		t.Fatal(err)
	}
	got := readableString(decoded)
	if got != expectedEncodedHeader {
		t.Errorf("WriteExchangeFile:\ngot: %q\nwant: %q", got, expectedEncodedHeader)
	}

	// Payload part
	if gotPayload := binExchange[3+cborLength:]; !bytes.Equal(gotPayload, expectedEncodedPayload) {
		t.Errorf("payload:\ngot %q,\nwant %q", gotPayload, expectedEncodedPayload)
	}
}
